package com.aurospaces.neighbourhood.bean;

public class OrderSymptomsBean {
	String orderId;
	String symptomsId;
	String symptomsDesc;
	public String getOrderId() {
		return orderId;
	}
	public void setOrderId(String orderId) {
		this.orderId = orderId;
	}
	public String getSymptomsId() {
		return symptomsId;
	}
	public void setSymptomsId(String symptomsId) {
		this.symptomsId = symptomsId;
	}
	public String getSymptomsDesc() {
		return symptomsDesc;
	}
	public void setSymptomsDesc(String symptomsDesc) {
		this.symptomsDesc = symptomsDesc;
	}

}
